export function getRandomFighters(): [any, any] {
  const shuffled = [...historicalFiguresDB].sort(() => 0.5 - Math.random())
  return [shuffled[0], shuffled[1]]
}

export function searchFigures(query: string) {
  if (!query.trim()) return historicalFiguresDB
  
  const lowercaseQuery = query.toLowerCase()
  return historicalFiguresDB.filter(figure =>
    figure.name.toLowerCase().includes(lowercaseQuery) ||
    figure.title.toLowerCase().includes(lowercaseQuery) ||
    figure.country.toLowerCase().includes(lowercaseQuery) ||
    figure.knownFor.toLowerCase().includes(lowercaseQuery)
  )
}

export function getFiguresByCountry(country: string) {
  return historicalFiguresDB.filter(figure => figure.country === country)
}
